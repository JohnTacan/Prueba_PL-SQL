  CREATE OR REPLACE NONEDITIONABLE PROCEDURE "USUARIO_JOHN"."ASIGNARLENGUAJES" IS
BEGIN
    FOR rec IN (SELECT id_programador FROM Programadores) LOOP
        DECLARE
            lang_ids SYS.OdciNumberList;
        BEGIN
            -- Randomly select 3 unique languages
            SELECT id_lenguaje BULK COLLECT INTO lang_ids
            FROM (SELECT id_lenguaje FROM Lenguajes ORDER BY DBMS_RANDOM.VALUE)
            WHERE ROWNUM <= 3;

            -- Loop through the selected languages and assign to the programmer
            FOR i IN 1..3 LOOP
                MERGE INTO ProgramadorLenguaje pl
                USING (SELECT rec.id_programador AS id_programador, lang_ids(i) AS id_lenguaje FROM dual) src
                ON (pl.id_programador = src.id_programador AND pl.id_lenguaje = src.id_lenguaje)
                WHEN MATCHED THEN
                    UPDATE SET puntos_experiencia = ROUND(DBMS_RANDOM.VALUE(0, 100))
                WHEN NOT MATCHED THEN
                    INSERT (id_programador, id_lenguaje, puntos_experiencia)
                    VALUES (src.id_programador, src.id_lenguaje, ROUND(DBMS_RANDOM.VALUE(0, 100)));
            END LOOP;
        END;
    END LOOP;
END;

