  CREATE OR REPLACE NONEDITIONABLE PROCEDURE "USUARIO_JOHN"."ASIGNAR_PROGRAMADORES_A_PROYECTOS" AS
    CURSOR c_proyectos IS
        SELECT p.ID_Proyecto, p.ID_Lenguaje
        FROM Proyectos p
        WHERE p.Estado = 'V';

    v_max_proyectos CONSTANT NUMBER := 2;
BEGIN
    FOR proyecto IN c_proyectos LOOP
        FOR programador IN (SELECT pl.ID_Programador
                            FROM Programadores_Lenguajes pl
                            WHERE pl.ID_Lenguaje = proyecto.ID_Lenguaje
                            ORDER BY pl.Puntos_Experiencia DESC) LOOP
            DECLARE
                v_proyectos_actuales NUMBER;
            BEGIN
                SELECT COUNT(*)
                INTO v_proyectos_actuales
                FROM Proyectos_Programadores pp
                WHERE pp.ID_Programador = programador.ID_Programador;

                IF v_proyectos_actuales < v_max_proyectos THEN
                    BEGIN
                        -- Inserta el programador en el proyecto si no excede el límite de proyectos
                        INSERT INTO Proyectos_Programadores (ID_Proyecto, ID_Programador, Usuario_Auditoria, Fecha_Auditoria)
                        VALUES (proyecto.ID_Proyecto, programador.ID_Programador, 'admin', SYSDATE);
                    EXCEPTION
                        WHEN DUP_VAL_ON_INDEX THEN
                            NULL; -- Ignorar si ya existe
                    END;
                END IF;
            END;
        END LOOP;
    END LOOP;
END Asignar_Programadores_A_Proyectos;

